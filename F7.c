int compression(int a[], int b[], int N)
{
    if (N <= 0) return 0;

    int idx = 0;          // индекс записи в массив b[]
    int cur = a[0];        // текущий символ последовательности
    int cnt = 0;           // длина текущего блока

    /* если первая цифра единица, записываем нулевой блок нулей */
    if (cur == 1)
        b[idx++] = 0;

    for (int i = 0; i < N; ++i) {
        if (a[i] == cur) {
            ++cnt;
        } else {
            b[idx++] = cnt;   // сохраняем длину предыдущего блока
            cur = a[i];
            cnt = 1;          // начинаем новый блок
        }
    }

    /* записываем последний блок */
    b[idx++] = cnt;

    return idx;            // количество элементов в массиве b[]
}